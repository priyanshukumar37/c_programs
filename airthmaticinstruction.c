#include<stdio.h>
#include<math.h>//power wala print karne ke liye math.h ka use karte hai
int main()
{
    int a = 4;
    int b = 8;
    printf("the value of a + b is: %d",a + b);
    printf("the value of a - b is: %d",a - b);
    printf("the value of a * b is: %d",a * b);
    printf("the value of a / b is: %d",a / b);
    printf("the value of 2 to the power of 5 is %f \n",pow(2,5));
    return 0;
}